class Solution {
public:
    bool checkValidString(string s) {
        // æ¨¡æ‹Ÿæ³•
        // ( += 1; ) -= 1
        int lo = 0;
        int hi = 0;
        for (char c : s) {
            if (c == '(') {
                lo += 1; hi += 1;
            } else if (c == ')') {
                lo -= 1; hi -= 1;
            } else {
                lo -= 1;
                hi += 1;
            }
            lo = max(lo, 0);
            if (lo > hi) return false;
        }

        return lo == 0;
    }
};
