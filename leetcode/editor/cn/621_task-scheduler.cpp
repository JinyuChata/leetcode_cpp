#include "bits/stdc++.h"
using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    int leastInterval(vector<char>& tasks, int n) {
        // å…ˆåšç»Ÿè®¡
        vector<int> cnts(26);
        for (int i = 0; i < 26; i++) cnts[i] = 0;
        for (char c : tasks) {
            cnts[c-'A']++;
        }

//        for (int i = 0; i < 26; i++) cout << cnts[i] << endl;

        // æ’åº
        sort(cnts.begin(), cnts.end(), [](const int& x, const int& y) { return x>y; });
        // å…ˆçœ‹æœ‰ç©ºå¯æ’çš„æƒ…å†µï¼Œæœ‰å¤šå°‘ä¸ªå¹¶åˆ—çš„
        int last = 0;
        while (last < 26 && cnts[last] == cnts[0]) {
            last++;
        }
        // æ¯”è¾ƒï¼šæœ‰ç©ºå¯æ’çš„æƒ…å†µsize å’Œ æ— ç©ºå¯æ’çš„æƒ…å†µ,å…¨éƒ¨é“ºæ»¡çš„size
        return max((cnts[0]-1)*(n+1)+last, (int)tasks.size());

    }
};
