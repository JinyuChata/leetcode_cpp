class Solution {
public:
    int numDistinct(string s, string t) {
        int ns = s.length();
        int nt = t.length();

        // dp[i][j]: s[0..i]ç»è¿‡åˆ é™¤åï¼Œå¾—åˆ°t[0..j]çš„æ–¹æ³•æ•°
        vector<vector<unsigned int>> dp(ns+1, vector<unsigned int>(nt+1, 0));
        dp[0][0] = 1;
        // åˆå§‹åŒ–ç¬¬ä¸€è¡Œï¼šdp[0][j] -> æ— é¡»åˆå§‹åŒ–ï¼Œéƒ½æ˜¯0
        // åˆå§‹åŒ–ç¬¬ä¸€åˆ—ï¼šdp[i][0]
        for (int i = 1; i <= ns; i++) {
            dp[i][0] = 1;   // åªæœ‰ä¸€ç§æ–¹æ³•ï¼šå…¨éƒ¨åˆ å®Œ
        }

        for (int i = 1; i <= ns; i++) {
            for (int j = 1; j <= nt; j++) {
                unsigned int tot = 0;
                // skip
                tot += dp[i-1][j];
                if (s[i-1] == t[j-1]) {
                    tot += dp[i-1][j-1];
                }

                dp[i][j] = tot;
            }
        }


        return dp[ns][nt];
    }
};
