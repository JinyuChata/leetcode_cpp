class Solution {
public:
    void moveZeroes(vector<int>& nums) {
        // æ ¸å¿ƒæ€æƒ³ï¼š[å·¦,å³] èŒƒå›´å†…ï¼Œå…¨æ˜¯0
        int n = nums.size(), left = 0, right = 0;
        while (right < n) {
            if (nums[right]) {
                swap(nums[left], nums[right]);
                left++;
            }
            right++;
        }
    }
};
