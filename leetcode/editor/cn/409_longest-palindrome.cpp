class Solution {
public:
    int longestPalindrome(string s) {
        int log[255] = {0};
        for (char c : s) {
            log[c] += 1;
        }

        int res = 0;
        for (int i = 'a'; i <= 'z'; i++) {
            res += (log[i] / 2 * 2);
        }
        for (int i = 'A'; i <= 'Z'; i++) {
            res += (log[i] / 2 * 2);
        }

        if (res < s.size()) res++;

        return res;

    }
};
